
public class MemberNotFoundFailure {
	private String msg;
	
	
	public MemberNotFoundFailure() {
		
	}


	public String getMsg() {
		return msg;
	}


	@Override
	public String toString() {
		return "MemberNotFoundFailure [msg=" + msg + "]";
	}


	public void setMsg(String msg) {
		this.msg = msg;
	}

}
